class Ckeditor::Picture < Ckeditor::Asset
  has_attached_file :data,
                    :default_style => :content,
                    :url  => "/ckeditor/pictures/:id/:style",
                    :path => ":rails_root/shared/:rails_env/ckeditor/pictures/:id/:style_:basename.:extension",
                    :styles => { :content => '800>', :thumb => '118x100#' }

  validates_attachment_size :data, :less_than => 2.megabytes
  validates_attachment_presence :data
  validates_attachment_content_type :data, content_type: ['image/jpeg', 'image/png', 'image/gif']

  def url_content
    url(:content)
  end
end
